    protected $code;

    /**
     * Country constructor.
     */
    public function __construct($id)
    {
        $this->id = $id;

        if ($country = self::$countries[$id]) {
            $this->name = $country['name'];
            $this->code = $country['iso_country_code'];
        }
    }

    /**
     * @param $id
     * @return \Gentor\Etsy\Helpers\Country
     */
    public static function getCountryById($id): Country
    {
        return new static($id);
    }

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @return string|null
     */
    public function getName(): ?string
    {
        return $this->name;
    }

    /**
     * @return string|null
     */
    public function getCode(): ?string
    {
        return $this->code;
    }
